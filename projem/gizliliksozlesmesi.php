Verileriniz nerede işleniyor?
Sizden topladığımız veriler genelde Avrupa Birliği veya Avrupa Ekonomik Alanında (“AB/AEA”) saklanır ancak gerekmesi durumunda AB/AEA dışındaki bir ülkeye de transfer edilebilir ve orada işlenebilir. Kişisel verilerinizin böyle bir şekilde aktarılması, ilgili kanunlara uygun olarak ve yasal haklarınız ihlal edilmeden gerçekleştirilecektir.

Zaman zaman kişisel verileri AB/AEA’dan, Avrupa komisyonu tarafından bu tür bir aktarım için güvenli ülke olarak onaylanmamış bir üçüncü ülkeye aktarabiliriz (yeterlilik kararı). Boutique, uygulanabilir olduğu takdirde, aktarım amacıyla AB/AEA’da veya diğer yasal gerekçelerde tanınan korumaya benzer düzeyde koruma sağlamak için Standart Sözleşme Maddelerini kullanacaktır. 

Verilerinize kim erişebilir?
Kişisel verileriniz, yalnızca hedeflenen işleme amacını yerine getirmek için verilere ihtiyaç duyanlar tarafından kullanılabilir ve erişilebilir durumdadır. Kişisel verileriniz, gerekli olduğu takdirde, Boutique Grup bünyesindeki şirketler ve markalar arasında, Boutique adına belirli görevleri yerine getiren tedarikçiler ve taşeronlarla (işleyiciler ve alt işleyiciler) ve bağımsız üçüncü taraflarla paylaşılabilir.

Buna ek olarak, bu bilgileri kullanmanın veya açığa çıkarmanın şu amaçlarla gerekli veya tavsiye edildiğine inanmak için nedenlerimiz olduğunda kişisel verileri üçüncü taraflara da açığa çıkarabiliriz: (i) olası yasa ihlallerine ilişkin soruşturmalar yürütmek; (ii) bizimle girdiği bir anlaşmayı ihlal eden birinin kimliğini belirlemek, kendisiyle iletişim kurmak veya kendisine karşı yasal işlem yapmak; (iii) güvenlik ihlallerini araştırmak veya yasal bir mesele uyarınca devlet makamlarıyla işbirliği yapmak veya (iv) dolandırıcılığın önlenmesi de dahil olmak üzere haklarımızı, güvenliğimizi veya mülkiyetimizi korumak.

Üçüncü bir tarafla birleşmemiz veya satın alınmamız, yeniden yapılanma gibi başka bir iş işlemine tabi tutulmamız veya bu tür bir işlemin önerilmesi durumunda hakkınızda sahip olduğumuz kişisel verileri aktarma hakkımızı saklı tutarız. 

İşleme konusundaki yasal dayanak nedir?
Geçerli bir yasal dayanak olmadan, Boutique’in kişisel veri toplama, işleme, kullanma, saklama vs. izni yoktur. Yasallık rızanızdan, sözleşmeden, yasal yükümlülüklerden veya bir işletme olarak meşru menfaatimizden kaynaklanabilir. Kişisel verilerin sunulmasının yasal gereklilik olması, bu verilerin sözleşme yapılmasını gerektirmesi, kişisel verilerin sunulmasının bir zorunluluk olması ve bunları yapmadığınız takdirde çeşitli sonuçlar doğuyor olması fark etmeksizin, sizden aldığımız kişisel verilerin işlenmesine dair her bir işlemin amacına, geçerli yasal dayanağa ve kullanabileceğiniz haklarınıza dair sizi bilgilendireceğiz. 

