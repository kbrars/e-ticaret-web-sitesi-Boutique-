Müşteri veri koruma ve gizlilik taahhüdü
Kişisel verilerinizin ve gizliliğinizin korunması H&M Group için en büyük önceliktir. Bu Gizlilik Bildiriminde Boutique Group müşterileriyle ilgili kişisel verilerin toplanması, kullanılması, işlenmesi, saklanması vb. konularda açık, özlü ve şeffaf iletişim sunmak istiyoruz.


Bu Gizlilik Bildiriminin anlamı kapsamında "Boutique Group müşterisi", bir Boutique Group bağlı şirketi veya markası, resmî web sitelerimizin ya da mağazalarımızın ziyaretçilerinden biri, bir bağlılık programı veya topluluk üyesi tarafından sunulan bir ürün ya da hizmetin eski, mevcut ve potansiyel müşterisi veya kullanıcısı anlamına gelir.

İlkeler
Boutique Grup gizlilik ve veri koruma taahhüdünü aşağıdaki ilkeleri benimseyerek beyan eder.

Boutique kişisel verileri yasal, adil, doğru ve şeffaf bir şekilde kullanır.
Boutique gereğinden fazla kişisel veri toplamaz ve yalnızca meşru bir amaç için veri toplar.
Boutique gerekliliğinden fazla veya gerekliliğinden uzun süre veri saklamaz.
Boutique kişisel verileri uygun güvenlik önlemleriyle korur.

Verilerinizi neden işliyoruz?
Kişisel verilerinizi sizinle bağlantılı olarak kullanır ve işleriz, örneğin ürünlerimizi çevrim içi veya mağazadan satın almak, web sitemizi ziyaret etmek veya müşteri hizmetleriyle iletişime geçmek. Ad, adres, e-posta adresi, telefon numarası, sosyal güvenlik numarası, ödeme bilgileri, satın alma, sipariş ve kullanım geçmişi, IP adresi, üye kimliği ve diğer durumla ilgili bilgiler (ör. müşteri hizmetlerimizle iletişim kurarken verdiğiniz bilgiler) kişisel veri örnekleridir.

Bu Gizlilik Bildiriminin her bir belirli bölümünde, her ilgili bilgi işleme amacı size bildirilecektir.  

Kişisel verilerinizin işlenmesinden kim sorumlu?
İsveç şirketi, Boutique Hennes & Mauritz GBC AB, bu Gizlilik Bildiriminin kapsamı dâhilinde kişisel verilerin işlenmesinden birincil olarak sorumludur.

Bu Gizlilik Bildiriminin her bir belirli bölümünde, kişisel verilerinizin işlenmesi, sorumlulukların atanması ve hakların kullanımına ilişkin usuller konusunda Boutique Hennes & Mauritz Tekstil Ltd Sirketi sorumlu olduğunda size bildirilecektir.   

